package com.zonekey.study.vo;

import com.zonekey.study.entity.ProcessInfo;
import com.zonekey.study.entity.Resource;
import com.zonekey.study.entity.SysUser;

public class ProcessView extends ProcessInfo {
	private static final long serialVersionUID = 1L;
	private String createdateString;
	private String modifydateString;
	private Resource resource;
	private SysUser user;

	public String getCreatedateString() {
		return createdateString;
	}

	public void setCreatedateString(String createdateString) {
		this.createdateString = createdateString;
	}

	public String getModifydateString() {
		return modifydateString;
	}

	public void setModifydateString(String modifydateString) {
		this.modifydateString = modifydateString;
	}

	public Resource getResource() {
		return resource;
	}

	public void setResource(Resource resource) {
		this.resource = resource;
	}

	public SysUser getUser() {
		return user;
	}

	public void setUser(SysUser user) {
		this.user = user;
	}
}
