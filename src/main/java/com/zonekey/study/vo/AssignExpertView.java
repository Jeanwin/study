package com.zonekey.study.vo;

import java.util.List;

public class AssignExpertView {
	private List<Integer> worksIds;
	private List<String> specialist;
	private int activeId;

	public List<Integer> getWorksIds() {
		return worksIds;
	}

	public void setWorksIds(List<Integer> worksIds) {
		this.worksIds = worksIds;
	}

	public List<String> getSpecialist() {
		return specialist;
	}

	public void setSpecialist(List<String> specialist) {
		this.specialist = specialist;
	}

	public int getActiveId() {
		return activeId;
	}

	public void setActiveId(int activeId) {
		this.activeId = activeId;
	}

	@Override
	public String toString() {
		return "AssignExpertView [worksIds=" + worksIds + ", specialist=" + specialist + ", activeId=" + activeId + "]";
	}
    
}
