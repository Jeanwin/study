package com.zonekey.study.vo;

import com.zonekey.study.entity.ReviewDetail;

public class ReviewDetailView extends ReviewDetail {
	private static final long serialVersionUID = 3720983359035347354L;

	private String username;
	//
	private String reviewdateString;
	private String createdateString;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getReviewdateString() {
		return reviewdateString;
	}

	public void setReviewdateString(String reviewdateString) {
		this.reviewdateString = reviewdateString;
	}

	public String getCreatedateString() {
		return createdateString;
	}

	public void setCreatedateString(String createdateString) {
		this.createdateString = createdateString;
	}

	@Override
	public String toString() {
		return "ReviewDetailView [username=" + username + ", reviewdateString=" + reviewdateString + ", createdateString=" + createdateString + "]";
	}

}
