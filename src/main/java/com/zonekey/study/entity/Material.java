package com.zonekey.study.entity;

/**
 * 素材类
 * 
 * @author Administrator
 * 
 */
public class Material {
	private String id;
	private String name;
	private String nametype;
	private String type;
	private String sort;// 排序
	private String flag;// '允许下载:1,允许,0不允许',
	private String resourceid;
	private String worksid;
	private String floder;
	private String curriculumid;
	private String fileurl;//图片地址，如：doc/2015/20150623/28ebf860-3872-44ab-a917-ac65bbc5b2d2.docx
	private String transferurl;//在线阅读转换后地址
	private String typeMaterial;
	private String worksidMaterial;
	private String floderMaterial;
	private String curriculumidMaterial;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getSort() {
		return sort;
	}

	public void setSort(String sort) {
		this.sort = sort;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getResourceid() {
		return resourceid;
	}

	public void setResourceid(String resourceid) {
		this.resourceid = resourceid;
	}

	public String getWorksid() {
		return worksid;
	}

	public void setWorksid(String worksid) {
		this.worksid = worksid;
	}

	public String getNametype() {
		return nametype;
	}

	public void setNametype(String nametype) {
		this.nametype = nametype;
	}

	public String getFloder() {
		return floder;
	}

	public void setFloder(String floder) {
		this.floder = floder;
	}

	public String getCurriculumid() {
		return curriculumid;
	}

	public void setCurriculumid(String curriculumid) {
		this.curriculumid = curriculumid;
	}

	public String getTypeMaterial() {
		return typeMaterial;
	}

	public void setTypeMaterial(String typeMaterial) {
		this.typeMaterial = typeMaterial;
	}

	public String getWorksidMaterial() {
		return worksidMaterial;
	}

	public void setWorksidMaterial(String worksidMaterial) {
		this.worksidMaterial = worksidMaterial;
	}

	public String getFloderMaterial() {
		return floderMaterial;
	}

	public void setFloderMaterial(String floderMaterial) {
		this.floderMaterial = floderMaterial;
	}

	public String getCurriculumidMaterial() {
		return curriculumidMaterial;
	}

	public void setCurriculumidMaterial(String curriculumidMaterial) {
		this.curriculumidMaterial = curriculumidMaterial;
	}

	public String getFileurl() {
		return fileurl;
	}

	public void setFileurl(String fileurl) {
		this.fileurl = fileurl;
	}

	public String getTransferurl() {
		return transferurl;
	}

	public void setTransferurl(String transferurl) {
		this.transferurl = transferurl;
	}

	@Override
	public String toString() {
		return "Material [id=" + id + ", name=" + name + ", nametype="
				+ nametype + ", type=" + type + ", sort=" + sort + ", flag="
				+ flag + ", resourceid=" + resourceid + ", worksid=" + worksid
				+ ", floder=" + floder + ", curriculumid=" + curriculumid
				+ ", fileurl=" + fileurl + ", transferurl=" + transferurl
				+ ", typeMaterial=" + typeMaterial + ", worksidMaterial="
				+ worksidMaterial + ", floderMaterial=" + floderMaterial
				+ ", curriculumidMaterial=" + curriculumidMaterial + "]";
	}

	
}
